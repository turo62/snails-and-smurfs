package com.codecool.api.partakens;

public class Smurf {
    private String name;
    private int weight;
    
    public Smurf(String name, int weight) {
        this.name = name;
        this.weight = weight;
    }
    
    public String getName() {
        return name;
    }
    
    public int getWeight() {
        return weight;
    }
}
